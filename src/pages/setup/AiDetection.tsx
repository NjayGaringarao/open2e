import InputBox from "@/components/InputBox";
import StepContainer from "@/components/setup/StepContainer";
import { useSetupNavigation } from "@/context/setup/navigation";
import { useSetupProcedure } from "@/context/setup/procedure";
import { useDialog } from "@/context/dialog";
import React, { useEffect, useState } from "react";
import clsx from "clsx";
import icon from "@/constant/icon";
import { Loading } from "@/components/Loading";
import { Check, X } from "lucide-react";
import Markdown from "@/components/Markdown";
import { validateApiKey } from "@/lib/gptZero/api";

const AiDetection = () => {
  const { navigate, step, totalSteps } = useSetupNavigation();
  const { gptZeroApiKey, setGptZeroApiKey } = useSetupProcedure();
  const [verifying, setVerifying] = useState(false);
  const [isApiValid, setIsApiValid] = useState(false);
  const [prompt, setPrompt] = useState<string>();

  const validate = async (apiKey: string) => {
    try {
      setVerifying(true);

      if (!gptZeroApiKey.length) {
        setPrompt(undefined);
        return;
      }

      const { error } = await validateApiKey(apiKey);

      setIsApiValid(!error);

      setPrompt(
        error
          ? "Incorrect API key provided or no internet connection. You can find your API key at [https://app.gptzero.me/api](https://app.gptzero.me/api)."
          : "Validation Success: API Key is Valid."
      );
    } catch (error) {
      setIsApiValid(false);
      setPrompt(
        "Incorrect API key provided or no internet connection. You can find your API key at [https://app.gptzero.me/api](https://app.gptzero.me/api)."
      );
    } finally {
      setVerifying(false);
    }
  };

  useEffect(() => {
    // DEBOUNCE
    const timeout = setTimeout(() => {
      validate(gptZeroApiKey);
    }, 600);
    return () => clearTimeout(timeout);
  }, [gptZeroApiKey]);

  return (
    <StepContainer
      step={step}
      totalSteps={totalSteps}
      onNext={navigate.next}
      onBack={navigate.back}
      disabledNext={!isApiValid && !!gptZeroApiKey.length}
      nextLabel={!!gptZeroApiKey.length ? "Next" : "Skip"}
    >
      <div className="">
        <h1 className="text-5xl font-semibold text-primary">AI Detection</h1>
        <p className="text-uGrayLight text-lg mt-4">
          Check if the answer was generated by AI.
        </p>
      </div>

      <div
        className={clsx(
          "p-4 rounded-md bg-panel",
          "flex flex-row gap-4 items-center"
        )}
      >
        <div className="h-full">
          <img
            src={icon.gptZero}
            alt="openai"
            className="h-12 w-auto bg-white p-1 rounded-md"
          />
        </div>
        <div className="flex-1">
          <p className="text-xl font-medium text-uGray">GPT-Zero</p>
          <p className="text-base text-uGrayLight">
            This will requires an active internet connection. API key is
            optional but recomended. Free tier is limited to 10,000 words per
            month â€” once exceeded, AI detection will be unavailable.
          </p>
          <div
            className={clsx(
              "flex-1 flex flex-col justify-center",
              "mt-4 gap-2"
            )}
          >
            <InputBox
              id="api-key"
              isPassword
              value={gptZeroApiKey}
              setValue={setGptZeroApiKey}
              placeholder="Paste your api key here..."
              inputClassName={clsx(
                "py-1 px-3 w-full",
                "border border-panel",
                "text-base font-mono"
              )}
            />

            {prompt && (
              <div className="flex flex-row gap-2 text-xs text-uGrayLight items-center">
                {verifying ? (
                  <Loading size="small" />
                ) : isApiValid ? (
                  <Check className="h-8 w-8 text-uGray" />
                ) : (
                  <X className="h-8 w-8 text-uRed" />
                )}
                <div className="font-mono">
                  <Markdown text={prompt} />
                </div>
              </div>
            )}
          </div>
        </div>
        <div className="px-4 items-center flex flex-col">
          <p className="text-xl font-mono text-uGrayLight">P500.00</p>
          <p className="text-xs font-mono text-uGrayLight -mt-1">Monthly</p>
        </div>
      </div>
    </StepContainer>
  );
};

export default AiDetection;
